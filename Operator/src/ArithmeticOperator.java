//Arithmetic Operators

public class ArithmeticOperator {

    public static void main(String[] args)
    {
        int a=5;
        float b=6.4f;

        System.out.println("a= "+a);
        System.out.println("b= "+b);

        //Addition
        System.out.println("a+b= "+(a+b));

        //substract
        System.out.println("a-b= "+(a-b));

        //multiplication
        System.out.println("a*b= "+(a*b));

        //Division
        System.out.println("a/b= "+(a/b));

        //remainder
        System.out.println("a%b= "+(a%b));
    }

}

/*
Output

a= 5
b= 6.4
a+b= 11.4
a-b= -1.4000001
a*b= 32.0
a/b= 0.78125
a%b= 5.0

 */